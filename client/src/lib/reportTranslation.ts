function translateReportTemplate(value: string, language: NonEnglishLanguage) {
  const trimmed = value.trim();
  const enableMatch = trimmed.match(/^Enable (.+) to deliver higher-impact outcomes in the (.+) role\.$/);
  if (enableMatch) {
    return value.replace(trimmed, reportTemplateTranslations[language].enableImpactRole(enableMatch[1], enableMatch[2]));
  }
  return null;
}

function phrasesFor(language: NonEnglishLanguage) {
  return {
    ...commonTerms[language],
    ...uiTerms[language],
    ...adminUiPhrases[language],
    ...adminSupplementalPhrases[language],
    ...adminReportSupplementalPhrases[language],
    ...generatedReportSupplementalPhrases[language],
    ...commonPhrases[language],
    ...uiPhrases[language],
  };
}

export function translateReportText(value: unknown, language: LanguageCode): string {
  if (typeof value !== "string") return "";
  if (language === "en" || shouldSkipText(value)) return value;

  const phraseMap = phrasesFor(language);
  const template = translateReportTemplate(value, language);
  if (template) return template;

  const exact = phraseMap[value] || phraseMap[value.trim()];
  if (exact) return value.replace(value.trim(), exact);

  return Object.entries(phraseMap)
    .sort(([left], [right]) => right.length - left.length)
    .reduce((text, [source, target]) => text.replace(new RegExp(escapeRegExp(source), "g"), target), value);
}

export function translateReportList(items: unknown, language: LanguageCode): string[] {
  if (!Array.isArray(items)) return [];
  return items
    .filter((item): item is string => typeof item === "string" && item.trim().length > 0)
    .map((item) => translateReportText(item, language));
}

function translateValue(value: unknown, language: LanguageCode, key?: string): unknown {
  if (typeof value === "string") {
    return key && skipKeys.has(key) ? value : translateReportText(value, language);
  }

  if (Array.isArray(value)) {
    return value.map((item) => translateValue(item, language));
  }

  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value as Record<string, unknown>).map(([entryKey, entryValue]) => [
        entryKey,
        translateValue(entryValue, language, entryKey),
      ])
    );
  }

  return value;
}

export function translateReportRecord<T>(value: T, language: LanguageCode): T {
  if (language === "en") return value;
  return translateValue(value, language) as T;
}
