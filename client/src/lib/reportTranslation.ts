function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function shouldSkipText(value: string) {
  const trimmed = value.trim();
  return (
    trimmed.length === 0 ||
    enumLikeValues.has(trimmed) ||
    /^[a-z]+(_[a-z]+)+$/.test(trimmed) ||
    /^(https?:|data:|blob:|mailto:)/i.test(trimmed) ||
    /^\d{4}-\d{2}-\d{2}/.test(trimmed) ||
    /^[\w.-]+@[\w.-]+\.\w+$/.test(trimmed) ||
    (/^[^\s]+\.[a-z0-9]{2,6}$/i.test(trimmed) && !trimmed.includes(" "))
  );
}

